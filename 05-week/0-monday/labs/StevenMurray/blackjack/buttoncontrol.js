function hideDeal() {
    $('#deal-button').hide();
}

function hideHitStand() {
    $('#hit-button').hide();
    $('#stand-button').hide();
}

function showButtonGroup() {
    $('#deal-button').show();
    $('#hit-button').show();
    $('#stand-button').show();
}

function hidePlayAgain() {
    $('#play-again-button').hide();
    $('#quit-button').hide();
}

function showPlayAgain() {
    $('#play-again-button').show();
    $('#quit-button').show();
}